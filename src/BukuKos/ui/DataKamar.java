    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // masukan data ke dalam database
        try {
            Connection con = Koneksi2.getConnection();
            //query insert data ke dalam database mysql
            pst = con.prepareStatement("INSERT INTO tb_kamar (id_kamar, lantai, fasilitas, tarif) VALUES(?,?,?,?)");
            //validasi ketika textField kosong

            if (txt_idkamar.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID Kamar tidak boleh kosong");
            } else if (txt_lantai.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Lantai tidak boleh kosong");
            } else if (txt_fasilitas.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Fasilitas tidak boleh kosong");
            } else if (txt_tarif.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tarif tidak boleh kosong");
            } else {
                //digunakan untuk memasukkan data ke masing2 variabel textfield seperti namaTxt, dll

                //misal bingung pst sama rs bisa diliat di variabel diatas
                pst.setString(1, txt_idkamar.getText());
                pst.setString(2, txt_lantai.getText());
                pst.setString(3, txt_fasilitas.getText());
                pst.setString(4, txt_tarif.getText());
                pst.executeUpdate();

                //setelah nginput data kasih ini biar textfieldnya kosong lagi
                txt_idkamar.setText("");
                txt_lantai.setText("");
                txt_fasilitas.setText("");
                txt_tarif.setText("");

                //setelah daftar muncul pop up dafatar berhasil dan akan tampil frame baru
                JOptionPane.showMessageDialog(null, "Tambah Data Berhasil");
            }

        } catch (Exception e) {
//            JOptionPane.showMessageDialog(null, "Error " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_btn_tambahActionPerformed

