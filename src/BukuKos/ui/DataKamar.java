    //penempatan dan pencocokan data tabel di database dan di tabel JTable
    private void fetchData() {
        ArrayList<DataKamar2> list = dataKamar();
        DefaultTableModel model = (DefaultTableModel) kamar_table.getModel();
        Object[] row = new Object[5];

        for (int i = 0; i < list.size(); i++) {

            row[0] = list.get(i).getId_kamar();
            row[1] = list.get(i).getLantai();
            row[2] = list.get(i).getFasilitas();
            row[3] = list.get(i).getTarif();
            model.addRow(row);
        }
    }

    //perintah untuk memasukkan data ke dalam tabel dan select data
    public ArrayList<DataKamar2> dataKamar() {
        ArrayList<DataKamar2> dataKamar = new ArrayList<>();
        try {
            Connection con = Koneksi2.getConnection();
            String sql = "SELECT id_kamar, lantai, fasilitas, tarif FROM tb_kamar";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            DataKamar2 DataKamar;
            while (rs.next()) {
                DataKamar = new DataKamar2(rs.getString("id_kamar"), rs.getInt("lantai"), rs.getString("fasilitas"), rs.getInt("tarif"));
                dataKamar.add(DataKamar);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return dataKamar;
    }

