    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        //action button untuk mengedit data
        try {

            Connection con = Koneksi2.getConnection();
            int row = kamar_table.getSelectedRow();
            String value = (kamar_table.getModel().getValueAt(row, 0).toString());
            String sql = "UPDATE tb_kamar SET id_kamar=?,lantai=?,fasilitas=?,tarif=? WHERE id_kamar= '" + value + "'";
            if (txt_idkamar.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID Kamar tidak boleh kosong");
            } else if (txt_lantai.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Lantai tidak boleh kosong");
            } else if (txt_fasilitas.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Fasilitas tidak boleh kosong");
            } else if (txt_tarif.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tarif tidak boleh kosong");
            } else {
                pst = con.prepareStatement(sql);
                pst.setString(1, txt_idkamar.getText());
                pst.setString(2, txt_lantai.getText());
                pst.setString(3, txt_fasilitas.getText());
                pst.setString(4, txt_tarif.getText());
                pst.executeUpdate();

                DefaultTableModel model = (DefaultTableModel) kamar_table.getModel();
                model.setRowCount(0);
                fetchData();
                txt_idkamar.setText("");
                txt_lantai.setText("");
                txt_fasilitas.setText("");
                txt_tarif.setText("");
                JOptionPane.showMessageDialog(null, "Edit Selesai");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btn_editActionPerformed

